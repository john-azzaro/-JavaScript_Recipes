// ABOUT APPLICATION, CONTROL FLOW, AND CONDITIONAL LOGIC...
// 1. There are three essential logical operators: &&, ||, and !.
//    
//       && -- "and" evaluates as true if both values are true.
//              for example: 
//                           const foo = true
//                           const bar = false
//                           const ree = true
//                           
//                           foo && bar;     // false
//                           foo && ree;     // true
//
//       || -- "or" evaluates as true if one of the values evaluates as true.
//              for example:                             
//                           const foo = true
//                           const bar = false
//                           const ree = true
//
//                           foo || bar       // true
//                           foo || ree       // false
//
//       !  -- "exclamation" negates a boolean value.
//              for example:
//                           const foo = true
//     
//                           foo;             // true
//                           !foo;            // false